<h2>Was nach dem Transport zu beachten ist</h2>

<h3>Akklimatisierung: Mindestens 2–4 Wochen warten</h3>
<p>Jedes Klavier reagiert auf Veränderungen in Temperatur und Luftfeuchtigkeit. Am neuen Standort braucht das Instrument Zeit, um sich an die neue Umgebung zu gewöhnen. Die idealen Bedingungen: Raumtemperatur <strong>18–24°C</strong>, relative Luftfeuchtigkeit <strong>40–60%</strong>. Erst nach dieser Eingewöhnungsphase macht ein Stimmen dauerhaft Sinn.</p>
<p><strong>Wichtig:</strong> Ein Stimmen unmittelbar nach dem Transport ist nicht sinnvoll und wird von den meisten Klavierbauern nicht empfohlen. Warten Sie mindestens 2–4 Wochen.</p>

<h3>Checkliste nach dem Transport</h3>
<ul>
  <li>Klavier am neuen Standort aufstellen – nicht in der Nähe von Heizkörpern, Zugluft oder direkter Sonneneinstrahlung</li>
  <li>Zustandsprotokoll nach dem Transport erstellen und mit dem Vorher-Protokoll vergleichen</li>
  <li>2–4 Wochen Akklimatisierungszeit abwarten</li>
  <li>Klavierstimmer beauftragen (ca. 100–160 CHF für Standard-Stimmung)</li>
  <li>Bei Flügeln: Beine, Pedale und Notenpult wieder korrekt montieren lassen</li>
</ul>

