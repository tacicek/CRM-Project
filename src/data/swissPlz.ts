/**
 * Filter PLZ entries by prefix — for autocomplete suggestions.
 * Returns max 10 results.
 */
export function filterPlzByPrefix(prefix: string): PlzEntry[] {
  if (!prefix || prefix.length < 2) return [];
  const clean = prefix.trim();
  return SWISS_PLZ
    .filter(e => e.p.startsWith(clean))
    .slice(0, 10);
}

/**
 * Get exact PLZ entry — for auto-fill when 4 digits entered.
 */
export function getPlzEntry(plz: string): PlzEntry | undefined {
  return SWISS_PLZ.find(e => e.p === plz);
}
